#include <iostream>
using namespace std;


int main()
{
	//转义字符

	//换行符 \n

	cout << "Hello World\n";
	

	//反斜杠\\

	cout << "\\" << endl;

	//水平制表符 \t     作用可以整齐的输出数据     1个制表符分配8个字符

	cout << "aaaa\thelloworld" << endl;
	cout << "aa\thelloworld" << endl;
	cout << "aaaaaa\thelloworld" << endl;

	system("pause");

	return 0;

}
